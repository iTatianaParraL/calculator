public class Calculator {


    public double add (double a, double b){
        
        return a + b;
    }
    
    public double subtract (double a, double b){
        
        return a - b;
    }


    public double multiplication (double a, double b){
    
    return a * b;
    }

    public double Division (double a, double b){
    
    return a / b;
    }

}
